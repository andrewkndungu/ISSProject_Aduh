    private void loginUser(){


        String email = email_et.getText().toString().trim();
        String password  = password_et.getText().toString().trim();

        if(TextUtils.isEmpty(email)){
          Toast.makeText(this,"Please enter your email address",Toast.LENGTH_LONG);
            return;
        }

        if(TextUtils.isEmpty(password)){
            Toast.makeText(this,"Please enter your password",Toast.LENGTH_LONG);
            return;
        }

        if(!android.util.Patterns.EMAIL_ADDRESS.matcher(email).matches()){
         Toast.makeText(this,"Invalid email address. Enter a valid email.",Toast.LENGTH_LONG);
            return;

        }



        if ( email.equalsIgnoreCase("admin@gmail.com")){
            startActivity( new Intent( getApplicationContext() , Admin.class));

        }

        progressDialog.setMessage("Logging in. Please wait...");
        progressDialog.show();

        firebaseAuth.signInWithEmailAndPassword(email, password)
                .addOnCompleteListener(this, new OnCompleteListener<AuthResult>() {
                    @Override
                    public void onComplete(@NonNull Task<AuthResult> task) {
                        if(task.isSuccessful()){
                          Toast.makeText(Login.this,"Login successful.",Toast.LENGTH_LONG);
                            finish();
                            startActivity(new Intent(getApplicationContext(), MainActivity.class));
                        }else {

                          Toast.makeText(Login.this,"Login error. Kindly check your credentials",Toast.LENGTH_LONG);

                        }
                        progressDialog.dismiss();

                    }
                });

    }


